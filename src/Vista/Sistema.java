    private void txtCantidadVentaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadVentaKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        if (!"".equals(txtCantidadVenta.getText())) {
            String codigo = txtCodigoVenta.getText();
            String descripcion = textDescripcionVenta.getText();
            int cant = Integer.parseInt(txtCantidadVenta.getText());
            double precio = Double.parseDouble(txtPrecioVenta.getText());
            double total = cant * precio;

            item = item + 1;
            DefaultTableModel modelo = (DefaultTableModel) TableVenta.getModel();

            // Validar si ya está en la tabla
            for (int i = 0; i < TableVenta.getRowCount(); i++) {
                if (TableVenta.getValueAt(i, 1).equals(descripcion)) {
                    JOptionPane.showMessageDialog(null, "El producto ya está registrado. Si deseas actualizarlo, elimínalo primero.");
                    return;
                }
            }

            // Crear la fila
            Object[] fila = new Object[5];
            fila[0] = codigo;
            fila[1] = descripcion;
            fila[2] = cant;
            fila[3] = precio;
            fila[4] = total;

            modelo.addRow(fila);
            TableVenta.setModel(modelo);
            TotalPagar();

            LimpiarVenta();
            txtCodigoVenta.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese la cantidad");
        }
    }
    }//GEN-LAST:event_txtCantidadVentaKeyPressed

