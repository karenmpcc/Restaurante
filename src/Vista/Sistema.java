    private void RegistrarVenta() {
    int id_cliente = Integer.parseInt(txtIdCV.getText());
    String nombre_cliente = txtNombreClienteVenta.getText();
    int id_restaurante = restDao.obtenerIdPorNombre(restauranteCombo.getSelectedItem().toString());
    String restaurante = restauranteCombo.getSelectedItem().toString();
    double monto = Totalpagar;

    Venta v = new Venta();
    v.setId_cliente(id_cliente);
    v.setNombre_cliente(nombre_cliente);
    v.setId_restaurante(id_restaurante);
    v.setRestaurante(restaurante);
    v.setTotal(monto);
    String fechaActual = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
    v.setFecha(fechaActual); // asegúrate de tener fechaActual como String con el formato adecuado

    Vdao.RegistrarVenta(v);
}
    private void RegistrarDetalle() {
    int id = Vdao.IdVenta(); // Obtener el ID de la venta insertada

    for (int i = 0; i < TableVenta.getRowCount(); i++) {
        String codigo = TableVenta.getValueAt(i, 0).toString();
        Productos prod = proDao.BuscarPorCodigo(codigo);
        int idProducto = prod.getId_producto();
        int cant = Integer.parseInt(TableVenta.getValueAt(i, 2).toString());
        double precio = Double.parseDouble(TableVenta.getValueAt(i, 3).toString());

        Dv.setId_producto(idProducto);
        Dv.setCantidad(cant);
        Dv.setPrecio(precio);
        Dv.setId_venta(id);

        Vdao.RegistrarDetalle(Dv);
    }
}
